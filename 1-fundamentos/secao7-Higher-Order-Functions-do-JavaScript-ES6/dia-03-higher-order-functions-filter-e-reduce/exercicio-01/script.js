const students = ['Pedro Henrique', 'Miguel', 'Maria Clara'];
const grades = [[9, 8, 10, 7, 5], [10, 9, 9, 10, 8], [10, 7, 10, 8, 9]];

const studentAverage = () =>
  grades
    .map((element, index) =>
    ({
      name: students[index],
      avarage: element.reduce((acc, crr) => acc + crr, 0) / element.length
    })
    );

console.log(studentAverage());